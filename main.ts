controller.A.onEvent(ControllerButtonEvent.Pressed, function () {
    if (showCharacterScreen) {
        Player_1 = sprites.readDataSprite(CurrentlySelectedCharacter, "Player")
        Player_2 = sprites.readDataSprite(CurrentlySelectedCharacter, "Player2")
        Player_1.setFlag(SpriteFlag.Invisible, false)
        Player_2.setFlag(SpriteFlag.Invisible, false)
        Player_1.setPosition(40, 30)
        Player_2.setPosition(40, 100)
        Player_1.ay = gravity
        Player_2.ay = gravity
        for (let value of sprites.allOfKind(SpriteKind.selectablecharacter)) {
            sprites.destroy(value)
        }
        for (let value of sprites.allOfKind(SpriteKind.Background)) {
            sprites.destroy(value)
        }
        for (let value of sprites.allOfKind(SpriteKind.Text)) {
            sprites.destroy(value)
        }
        showCharacterScreen = false
    } else {
        if (Player_1.isHittingTile(CollisionDirection.Bottom)) {
            Player_1.vy = -175
        }
    }
})
